const formats = ["Paperback", "Hardcover", "Kindle"];

const addMeta = (category, books) =>
  books.map((book, index) => {
    const number = index + 1;
    return {
      ...book,
      id: `${category.toLowerCase().replace(/\s+/g, "-")}-${number}`,
      category,
      title: book.title ?? `${category} Book ${number}`,
      author: book.author ?? `${category} Author ${number}`,
      publisher: book.publisher ?? `BookStore Publications`,
      language: book.language ?? "English",
      format: book.format ?? formats[index % formats.length],
      pages: book.pages ?? 180 + number * 12,
      isbn: book.isbn ?? `978-93-${String(100000 + number).slice(-6)}-${number}`,
      description:
        book.description ??
        `A popular ${category.toLowerCase()} title with engaging storytelling and reader-friendly language.`,
    };
  });
  


